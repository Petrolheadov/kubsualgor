s = str(input('введите! \n'))
# Третий символ
print(s[2])
# Предпоследний символ
print(s[-2])
# Первые пять символов
print(s[:5])
# Вся строка, кроме последних двух символов
print(s[:-2])
# Символы с четными индексами
print(s[::2])
# Символы с нечетными индексами
print(s[1::2])
# Символы в обратном порядке
print(s[::-1])
# Символы через один в обратном порядке
print(s[-1::-2])
# Длина строки
print(len(s))
